const { Schema, model } = require('mongoose');

const MessageSchema = new Schema({
  content: {
    type: String,
    required: true,
  },
  from: {
    type: String,
    required: true,
  },
  to: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    required: true,
  },
  updatedAt: {
    type: Date,
    required: true,
  },
});

module.exports = Message = model('Message', MessageSchema);
